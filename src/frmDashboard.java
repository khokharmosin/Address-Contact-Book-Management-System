    private void cmbsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbsearchActionPerformed
        // TODO add your handling code here:
              DefaultTableModel data = (DefaultTableModel) tblMame.getModel();
              
              for (int i = data.getRowCount()-1; i>=0;i--){
                  data.removeRow(i);
              }
        try {
            c.select("SELECT `Name` FROM `addcontact` WHERE `contactgroup`='" + cmbsearch.getSelectedItem().toString().trim() + "' AND `user_email`= '" + user_email.trim() + "'");
            while (c.rs.next()) {                
                Object[] row = {c.rs.getString("Name")};
                data.addRow(row);
                
            }            
        } catch (SQLException ex) {
            Logger.getLogger(frmDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbsearchActionPerformed

    private void tblMameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMameMouseClicked
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) tblMame.getModel();
        int i = tblMame.getSelectedRow();
        
         try {
            c.select("SELECT * FROM `addcontact` WHERE `Name`='" + model.getValueAt(i, 0).toString().trim() + "'");

           while (c.rs.next()) {
                txtVName1.setText(c.rs.getString("Name"));
                txtVphone.setText(c.rs.getString("Phone-No"));
               txtVaddress.setText(c.rs.getString("Address"));
                txtvemailid.setText(c.rs.getString("Emali-Id"));
                txtVstate.setText(c.rs.getString("State"));
                cmbvmonth.setSelectedItem(c.rs.getString("DateOfBirth"));
                cmbvyear.setSelectedItem(c.rs.getString("DateOfBirth"));
                txtVzipcode.setText(c.rs.getString("ZipCode"));
                txtVage.setText(c.rs.getString("Age"));
                gen = c.rs.getString("Gender");
                if (gen.equals("Male")) {
                    rdVmale.setSelected(true);
               } else {
                   rdvfemale.setSelected(true);
               }

            }

       } catch (SQLException ex) {
          Logger.getLogger(frmDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_tblMameMouseClicked

